from dijkstra import dijkstra


def test_dijkstra():
    input_data = [
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 2, 1, 2, 3, 5, 7, 100],
        [100, 1, 0, 4, 4, 6, 8, 100],
        [100, 10, 4, 6, 5, 6, 7, 100],
        [100, 19, 13, 12, 9, 7, 8, 100],
        [100, 28, 21, 12, 12, 8, 9, 100],
        [100, 29, 20, 11, 10, 9, 10, 100],
        [100, 100, 100, 100, 100, 100, 100, 100]
    ]

    dist = [
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 100, 100, 100, 100, 100, 100, 100]
    ]

    expected = [
        [100, 100, 100, 100, 100, 100, 100, 100],
        [100, 2, 1, 2, 3, 5, 7, 100],
        [100, 1, 0, 4, 4, 6, 8, 100],
        [100, 10, 4, 6, 5, 6, 7, 100],
        [100, 19, 13, 12, 9, 7, 8, 100],
        [100, 28, 20, 11, 12, 8, 9, 100],
        [100, 29, 20, 11, 10, 9, 10, 100],
        [100, 100, 100, 100, 100, 100, 100, 100]
    ]

    answer = dijkstra(input_data, dist, (2, 2), (3, 5))
    assert answer == expected
